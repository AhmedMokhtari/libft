#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t dstsize)
{
	size_t	return_val;
	size_t	i;

	return_val = ft_strlen(src);
	i = 0;
	if (dstsize < 1)
		return (return_val);
	while (src[i] && i < dstsize - 1)
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (return_val);
}
